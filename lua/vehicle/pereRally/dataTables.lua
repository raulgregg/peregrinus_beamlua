local function initializeRandomParameters()
    for _, vehicle in ipairs(vehicles) do
        local path = vehicle.path
        randomizedValues.safetyDistance[path] = math.random(vehicle.safety[1], vehicle.safety[2]) / 1000
        randomizedValues.lateralOffsetRange[path] = math.random(vehicle.lateralOffset[1], vehicle.lateralOffset[2]) / 1000
        randomizedValues.lateralOffsetScale[path] = math.random(vehicle.lateralScale[1], vehicle.lateralScale[2]) / 1000
        randomizedValues.shortestPathBias[path] = math.random(vehicle.shortestPath[1], vehicle.shortestPath[2]) / 1000
        randomizedValues.risk[path] = math.random(vehicle.risk[1], vehicle.risk[2]) / 1000
        randomizedValues.vision[path] = math.random(vehicle.vision[1], vehicle.vision[2]) / 1000
        randomizedValues.awareness[path] = math.random(vehicle.awareness[1], vehicle.awareness[2]) / 1000
        randomizedValues.springForce[path] = math.random(vehicle.springForce[1], vehicle.springForce[2]) / 100
        randomizedValues.turnForce[path] = math.random(vehicle.turnForce[1], vehicle.turnForce[2]) / 100
        randomizedValues.awarenessForce[path] = math.random(vehicle.awarenessForce[1], vehicle.awarenessForce[2]) / 1000
    end
end


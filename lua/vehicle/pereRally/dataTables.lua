local vehicleLookup = {}
for _, vehicle in ipairs(vehicles) do
    vehicleLookup[vehicle.path] = vehicle
end

local checkpointMultiplierLookup = {
    byMapSeriesClass = checkpointMultiplierTable,
    getMultiplier = function(map, series, class, checkpoint)
        -- Validate all parameters first
        if not (map and series and class and checkpoint) then
            print("Missing parameters for multiplier lookup!")
            return 1.0
        end
        
        -- Safely navigate nested tables
        local mapData = checkpointMultiplierTable[map] or {}
        local seriesData = mapData[series] or {}
        local classData = seriesData[class] or {}
        local multiplier = classData[checkpoint]
        
        -- Validate multiplier value
        if type(multiplier) == "number" and multiplier > 0 then
            return multiplier
        end
        
        print(string.format("No valid multiplier found for %s/%s/%s/%d", 
            map, series, class, checkpoint))
        return 1.0
    end
}

local driveStyleLookup = {
    getDriveStyle = function(map, series, class, checkpoint)
        -- Safely navigate the nested tables with defaults
        return (((checkpointOffroad[map] or {})[series] or {})[class] or {})[checkpoint] or "default"
    end
}

